#include <iostream>
using namespace std;

int main() {
	cout << "|\\_/|" << endl;
	cout << "|q p|   /}" << endl;
	cout << "( 0 )\"\"\"\\" << endl;
	cout << "|\"^\"`    |" << endl;
	cout << "||_/=\\\\__|" << endl;
}

/*
""는 출력문안에 내용물을 쓸때 사용되므로 "만 사용해서는 출력문에 나오지 않기때문에
내가 원하는 " 그대로 출력할려면 \을 앞에 입력하여 기능을 취소해야 한다.
*/